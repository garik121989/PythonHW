#տրված ինդեքսով տարրը եւ վերադարձնում նոր զանգվածը որպես արդյունք։

def func(ls:list, in_dex:int)->list:
    for i in range(len(ls)):
        if i == in_dex:
            del ls[i]
    return ls

print((func([1,2,3,4,5], 0)))